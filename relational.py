# 关系运算

print('1 == 1? {}'.format(1 == 1))
print('2 != 3? {}'.format(2 != 3))
print('1 < 2? {}'.format(1 < 2))
print('1 <= 1? {}'.format(1 <= 1))

# 与 C 语言不同，Python 允许连续比较
print('1 < 2 <= 3? {}'.format(1 < 2 <= 3))
print('1 < 2 and 2 <= 3? {}'.format(1 < 2 and 2 <= 3))
x = -1
y = -2
print('x, y are both non-positive? {}'.format(x <= 0 >= y))
